public class Account
{
        private int regNumber;
        private String name;
        private int balance;


        public int getRegNumber()
            {return regNumber;}
        public String getName()
            {return name;}
        public int getBalance()
            {return balance;}



        public void setRegNumber(int r)
            {regNumber = r;}
        public void setName(String n)
            {name = n;}
        public void setBalance(int b)
            {balance = b;}
}